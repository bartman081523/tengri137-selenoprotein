def decode(period):
    pairs = [int(period[i:i+2]) for i in range(0, len(period)-1, 2) if i+2 <= len(period)]
    text = ''.join(PERIODIC[p][0] if p in PERIODIC else '?' for p in pairs)
    return text, pairs

